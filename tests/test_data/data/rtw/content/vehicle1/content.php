	<div id="main_content">
		<p>Our choice of a new adventure vehicle came on the heals of a 
		good experience with our Provan Tiger during our 19 month, 60,000 mile 
		<a href="/theamericas">Americas Trip</a> and in the face of a recent purchase (as it was then),
		and  a happy experience with a used 2008 Earthroamer.</p> 
		<p>Not long after purchasing the ER, with the adrenalin of Overland Expo 2011 still flowing 
        in our veins we decided a new multi-year multi-country adventure was called for.</p>

        <p>That raised the question of whether the ER was the <strong>right vehicle</strong> for the 
        proposed adventure or should we be considering something different.</p>

		<p>Ultimately we chose to (try and) sell our Earthroamer and commissioned a vehicle 
		from <a href="http://www.globalxvehicles.com">Global Expedition Vehicles</a>
		(here after called GXV).</p>
		<p><a href="http://www.globalxvehicles.com">GXV</a> sourced a 2003 Unimog U500 for us and we 
		contracted them to fit out that chassis with one of their <em>Safari</em> camper boxes
		and a range of other goodies.</p>
		<p>After a year of trying to sell our ER on consignment with the Earthroamer folks,
		and with prices falling, we elected to keep the ER as our US home.

		</p>
<p>I will post more information about the new vehicle as the construction takes place and after
		that as we use it and are in a position to assess the strengths and weaknesses.</p>
		

