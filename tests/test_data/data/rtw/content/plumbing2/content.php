	<div id="abstract">
    <p>The Plumbing system</p>
    </div>
<div id="main_content">

<p>It seems like the first article about the plumbing in our GXV camper was <a href="/posts/rtw/aneurysm">Aneurysm</a> published back in early 2013. That is why this one is called <em>Plumbing, Part 2</em>. I was prompted to write this post by a recent water leak we had in the camper.
That got me thinking about the other couple of problems we have had with the fresh water system over the past 1-1.5 years and the lessons 
I have learned from those incidents.</p>   
<p>Because of the <a href="/posts/rtw/aneurysm">Aneurysm</a> incident I had purchased the tools necessary to deal with the PEX plastic
tubing that is used in all the campers fresh water (both hot and cold) plumbing. In addition to the tools the incident also
made me aware that I should probably carry a selection of fittings for joining such tubing and some spare tubing. All of this turned 
out to be a good thing. But with the benefit of hind site I did not learn the lesson well enough as I did not carry enough spares.</p>
<p>The next plumbing lesson occurred in Russia in April/May of 2013, that is just after the start of our journey across Russia and Central 
Asia. One morning in a Russian truck stop I noticed water leaking from under the camper onto the right rear fender.
At the time the plumbing hoses and tubes under the camper were covered by a protective metal shield so it was not immediately
obvious where the water was coming from. Also at the time I was not particularly familiar with the fresh water system and could
not easily deduce the source of the water. But over the course of a few days I eventually worked out that the water was coming
from the overflow/pressure relief valve attached to the systems hot water tank.</p>
<p>After some pondering I guessed that the pressure relief valve was probably faulty and was opening under too low a pressure. I could
observe that when the water pump was turned on the valve would eventually "pop" and let water start flowing out of the over flow.</p>
<p>After some searching through my spare part box I discovered that I had the parts necessary to block or close the overflow line.
I figured that blocking the overflow from the hot water tank would be OK as a field fix as the only sources of pressure in the
tank where from the pump and from heating the water while the truck was driving or running the camper heating system. 
I figured I could control the pressure by turning off the pump
and leaving one of the hot water faucets open.</p>
<p>However what I found was unexpected. With the overflow blocked I turned on the water pump. I expected the pump to 
stop after a little while once it had built up to its operating pressure of 45-55 psi. But the pump did not shut off, 
the water tank started to "pop" and bulge. I quickly switched the pump off and let some pressure out of the 
tank through a fresh water faucet.</p>
<p>So now I had a new take on the issue. It was or at least may not be the pressure relief valve that was at fault but the pump
that would not turn off once it reached its supposed operating pressure. I repeated the experiment a number of times and simply
confirmed the observation. The pump continued to build pressure until
it popped the pressure relief valve - which is exactly what the pressure relief valve is supposed to do - the problem
was the pump not turning off once it had built operating pressure.</p>  
<p>With no prospect of a replacement pump on the horizon we improvised a solution. While driving (which heated the hot water tank),
or running the camper heating system (which also provided heat for the hot water tank) we would turn the water pump off,
and open one of the hot water faucets. This would prevent over-pressure in the hot water tank.</p>

<p>When water was required for whatever purpose we would open the appropriate faucet (including allowing some hot water out)
and then turn on the pump. We basically had a water system where all water flow was started and stopped with the water pump switch.</p>

<p>This situation "worked" and we persisted with it until after our breakdown in Yakutsk. During the repairs associated with
that incident GXV sent us a replacement for the water pump and pressure relief valve.</p>
<p>With the new pump and valve everything worked as expected.</p>
<h4>Lessons</h4>
<ul>
<li><p>if your camper uses PEX tubing carry the necessary tools, some spare tubing and an assortment of fittings and the clamps that fasten the tubing to the fittings.</p></li>
<li><p>I was, and am still, surprised by the problem with the pump. I have previously run Sureflow pumps in motorhomes for 
years without problems. Carry a spare, though to be honest I have not really learned this lesson as right now I don't have a spare pump
or pump head.</p></li>
<li><p>Diagnosis of this problem was made more difficult by the metal protective cover that hid the under camper plumbing. This
would not have been a problem except that it was difficult (read impossible in a field repair situation) to remove for a better look.
The lesson is to make "investigation" as easy as practical and to have covers and panels that "come off". The offending covers
have since been removed.</p></li>
<li><p>During this exercise I had the first inkling that PEX with brass fittings and ring clamps were not the easiest things
to deal with. Once connected the fittings are very difficult to take apart, first the ring clamp has to be broken off the tube
and then the deformed tube has to be pulled off the fitting. This last step is so difficult that generally the entire fitting
is simply "cut out" and a new assembly installed.</p> 
<p>Re-installing in tight quarters is also no easy thing as the crimping tool has to be placed right over the tubing and the
ring clamp squeezed down. This topic will come up again. PEX seems good, the ring clamps seem good in permanent situations or
situations where access is easy. But in tight quarters and where repeated disassembly may be required it is not the ideal
means of connection.</p></li>   
