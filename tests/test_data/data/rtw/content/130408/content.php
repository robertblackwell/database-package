	<div id="title">Paper work and sightseeing</div>
	<div id="abstract"></div>
	<div id="excerpt"></div>
	<div id="main_content">
	<p>We were up and at breakfast earlier this morning, not only attracted by the bright sunshine but also in anticipation
	of starting the process of getting the truck. At 9:00 we met with Svetlana and an insurance agent. Over the weekend
	Svetlana had prepared all the documents we would need for releasing the truck and the insurance agent was going to provide
	the last piece of the puzzle - 3 months insurance for 3000 rubbles (a bit under $100).</p>
	<p>Then it was off to the port where we called on FESCO to get a final copy of the "released" Bill of Lading (
	this signifies that the shipping company FESCO are happy that they have been fully paid) and then to customs to lodge
	all those documents Svetlana had prepared. We sat in the waiting area while Svetlana hopped around from one official to 
	another but within 40 minutes that process was complete. Next steps; her boss Yuri had to do something related to our
	truck that she was not authorized to do and Yuri and I would need to present ourselves at FESCO late in the afternoon
	for me to sign some more papers. Until then we were free.</p>
	<p>So it was tourist time and the day was encouraging, bright warm sun with a slightly chilly wind. A walk along the
	main drag (Svetlanskya), a visit to a submarine museum (S-56) and lunch at a cafe/pizzeria - nice pizzas.
	This was followed by a ride on the Funicular to the high point of the city where some buildings associated with
	the Far Eastern University are located (and a lot of students indicating that not all classes have been
	moved to Russkii Island) and from where we had a wonderful view of the city.</p>
