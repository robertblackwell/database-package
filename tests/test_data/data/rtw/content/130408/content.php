	We really did not understand what happened at FESCOs office except that there seemed to be some change in procedure
	and one of the office staff had to take some of the documents to the port storage yard where our vehicle is parked.
	He undertook to call Yuri when that was done. Indeed we heard from Yuri a couple of hours later
	that the papers had been delivered and that he (Yuri) would collect us at 10:00am tomorrow to go get the truck.</p>
	<p>Around 8:30pm Vladimir dropped by on his way home from work to make some suggestions of people
	and places we could visit. He had tentatively arranged with some of his friends along our route
	to meet with us and show us some of the local sights. As you can imagine we are delighted by this opportunity
	and responded very enthusiastically; though we both felt a little embarrassed as we have no way of repaying his 
	hospitality.  </p>
