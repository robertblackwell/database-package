		<p>The mornings drive was punctuated by a stop for fuel (again a note in Russian setting out pump number
		and liters of fuel required worked perfectly) and a stop for a few goodies at a small grocery
		store where we discovered another Nina serving us goods. 
		Road conditions varied from good first class pavement to terrible gravel where road construction
		was underway. There was evidence everywhere of a big effort to improve this road with many sections
		where trees had been cleared and early road preparation was underway. The scenery was stark. The trees 
		still showing their winter coat with no sign yet of spring. There was patchy snow on the ground and
		where the land was not snow covered it was wet. Many of the rivers and streams were still covered
		by some ice; though it appeared to be soft and slushy.</p>
		<p>Around midday we stopped at one of the periodic roadside parking places (well signposted)
		and after lunch set about trying to find the source of the water problem. Initially I 
		tried to get access to the house water tank; a difficult quest that I eventually abandoned
		before realizing that the leak appeared to be from the hot water tank's pressure relief valve.
		It appeared to be allowing water to pass whenever the pump was on. With that discovery
		we left the problem to percolate around in our brains while we did some more miles.</p>
	<div id="camping">
		<p>Throughout the afternoon we saw only a few places we deemed suitable as over night stops
		and with the day coming to an end and towns becoming fewer we determined that we would find
		somewhere to stop in <em>Lushegorsk</em> (I apologize for my perhaps poor anglisization
		of this towns name - I have not yet worked out how to put Cyrillic script into my pages).
		Inside the town we passed a couple of unlikely fuel stations before stopping in front of
		a large lot occupied by a number of 6x6 buses (that's right, buses - its a statement about
		the quality of the minor roads in the area). Again with Russian note in hand (this time,
		"can we park here for the night") I approached a couple of locals and was told "No"
		(and a lot of other stuff I could not hope to understand) and was pointed back down the 
		road in the direction of a large blue building that I think was some form of low budget hotel.
		After a discussion with the gate keeper and an exchange of 100 rubbles we had a place for the night
		among the trucks again. But hey -- better than by ourselves on the side of the road.</p>
