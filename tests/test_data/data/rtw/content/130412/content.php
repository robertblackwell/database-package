	<div id="title">A little problem, first night unaided</div>
	<div id="abstract"></div>
	<div id="excerpt"></div>
	<div id="main_content">
        <p>Last night among the trucks was surprisingly quiet, we heard a few come and go in the night
        but we did not (like often in the US) have refrigeration trucks to assault our ears. </p>
        <p>We prepared for the day in a leisurely manner, coffee, breakfast and only then noticed that
        our house water pump seemed to be acting up. Or more accurately not turning off as it should.
        After a bit of experimentation the problem escalated into a gush of water from somewhere under
        the camper every time the pump was turned on. As you might imagine this development was not
        welcome. Here we are about a week into an 8 month adventure and we already have a mechanical problem.
        But, somewhat uncharacteristically, rather than try fix the problem immediately we decided to get some 
        miles (or is it kilometers) under our belt and approach the problem in more measured fashion.</p> 	
