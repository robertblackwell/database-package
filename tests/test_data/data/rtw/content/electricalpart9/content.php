<h2>An update after nearly 2 years</h2>
<p>This is a maybe overdue update or exposition of our long term experience with the electrical system of our GXV truck. </p>
<p>Since
	the last post on this topic we have completed our planned journey across Siberia, Central Asia, Turkey and Europe. In addition
	we have crossed part of North America (Halifax Canada, to Flagstaff AZ, then to Baltimore, MD) and are on our way through the Baltic and
	Scandinavian States. In all we have been living pretty much full time in the truck for 2 years. So what has our experience been in 
	regard to the electrical system.</p>
<p>The one liner would be that the system has lived up to (indeed exceeded) our most optimistic expectation.
</p>
<p>The primary goal or mission of the electrical system was clearly stated in part 1 of this series
<a href="/vehicle/rtw/electricalpart1">Electrical system part 1</a> and was to.
<ul>
<li><p> operate without shore power for extended periods,  and</p></li>
<li><p>to use any shore power when available.</p></li>
</ul>
<p>In regard to the first of these we departed Vladivostok Russia on April 7th 2013 and did not arrive in our first camping ground,
with shore power, 
in Turkey until November 5th.</p>
<p>During that 7 month period we had shore power only twice. For a couple of days in June when the truck was parked 
inside a building in Ulaanbataar, Mongolia and similarly in Iran for two nights when the truck was parked at a workshop for repairs.
</p>
<p>So for all practical purposes the truck operated without shore power for 7 months. During that period we used the generator
sparingly. Mainly for camper A/C and microwave cooking. The house batteries were brought to full charge almost every day
using a combination of solar and the B2B device operating from the truck alternator.</p>
<p>Once into Turkey in November and December we had shore power from time to time but often camped wild. During this period we
used the generator more frequently as there was little or no contribution from solar (being winter) and we had periods when 
we did not drive for a couple of days at a time.</p>
<p>It is worth pointing out that in Mongolia, Iran, Turkey and thence Europe we successfully used shore power despite
variation in local mains voltage. I cannot even remember the voltage in Mongolia and Iran but Turkey and Europe are certainly
230V/50Hz. Our Mastervolt 100 amp charger handled (and continues to handle) these non-standard mains voltages for us
now that we are in Scandinavia.</p>
<p>Hence my conclusion, that the system did everything we expected from it and more.</p>
<p>The one area in which I thought the system was weak was in the area of solar capacity. Interestingly this showed up in situations of hot climatic conditions. While a hot summers sun in a place like Kyrgyzstan provided good input to the solar panels the heat also
caused the refrigerator to make high electrical demands. In sunny cool spring conditions in Siberia the solar panels
could supply the refrigerator's demands. But as temperatures increased in Central Asia the demand by the refrigerator over powered
the solar panels and we had to either drive the truck or run the generator.</p>
<p>On returning to the US in February-June 2014 I had an additional solar panel added (making a total of 3). This has made a remarkable
improvement in the ability of the solar panels to replace our daily usage; to the extent that on most days (during Scandinavian summer)
I do not need to use the truck alternator (nor generator or shore power) to recharge the house batteries.</p>
<p>Thus I also deem the extra solar panel an unqualified success.</p>
<p>It is worth noting that my belief in the need for a generator has not been diminished. During Nov, Dec 2013 and Jan, Feb 2014 we traveled
in Europe and North America. Not surprisingly the sun was very scarce and we only rarely found shore power. In these circumstances
if one does not drive each day (or run the truck engine) the only way of bringing the house batteries back to full charge is a generator.</p>        
