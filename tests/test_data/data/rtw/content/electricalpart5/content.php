<h2>Sterling-Power Battery to Battery Charger</h2>
<p>I initially installed a single Battery-Battery Charger (B2B) in the camper in November 2012
but that original B2B failed within a week and was returned. I installed the replacement in
mid December 2012 and used it for travel through the US South West and the Baja during December 2012 and January 2013.
To date the replacement has continued to function without problems.</p>
<p>At Quartzsite in Jan 2013 I met some representatives of Sterling-Power and following discussions
purchased and installed a second B2B to operate in parallel with the first. To date I
have not had a chance to test the pair in a meaningful way.</p>
<p>My experience with a single B2B is that it basically works as advertised. It draws on the alternator
via the starting batteries and provides charging to the AGM house batteries. I have observed it on
a number of occasions bring the AGM house batteries back to full charge (as indicated by the FLEXnetDC). 
</p>
<p>My only disappointments with the B2B is that it does not seem to ever reach its rated charging output
of 45 amps. I have not seen greater than 30 amps even when recharging a battery pack that is down
to 75% SOC.</p>
<p>Sterling has advertised a new B2B rated at 100amps and I would have loved to try one of these,
but they will not be available in the US before we ship to Russia. Hence as an
alternative (recommended by the Sterling reps at Quartzsite) 
I have installed a second 45 amp B2B in the hope that the two in parallel will
provide more charging current. I have yet to reach a conclusion on the effectiveness of this
approach.</p>
<p>I will almost certainly buy a 100 amp version when it is available because despite my one disappointment
this is a perfect device for boondockers.</p>


