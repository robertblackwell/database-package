    <p>It has/had been our plan for some time that we would get our Visas for Iran, Uzbekistan and Tajikistan while in Bishkek.
    So on Monday morning (29th) we set off to the Iranian Embassy armed with our passports and the email from the Iran
    visa agency that contained the visa approval number from the Iran Ministry of Foriegn Affairs.</p>
    <p>The young lady behind the counter gave us an application form to complete and then handed us a very, very, small piece of paper
    that contained the address of a local branch of the National Bank of Pakistan where we were to pay the visa issuance fee. 
    At the same time she told us that we needed to provide passport sized photos for the visa.</p>
    <p>This last requirement was a bit of a surprise as we understood from the visa agency that 
    in her visa photo Nina had to be wearing a scarf to cover her head and that the Embassy would make that
    photo themselves. </p>
    <p>So we set off down the street towards the Pakistan Bank looking for a place to get a visa photo. Luck was on our side
    and we got the photo within 15 minutes and at the bank it took about 30 minutes to get served and obtain
    the necessary receipt.</p>
    <p>Back at the Iranian Embassy all the paper work seemed to be in order and we were about to submit everything when we were
    told "come back in 5 days". No that was not going to work for us - "can we pay more money and get it faster?",
    was the question. So back to the Pakistan Bank and pay some more money for the "express service (1 day)". </p>
    <p>After the second visit to the bank I got back to the Iranian Embassy 5 minutes before they closed for lunch.
    However the hiccups were not over as it seemed that Tuesday was a holiday for the Embassy and so the earliest we could get
    back our passports with those Iran visas was 11:00am Wednesday 31st.</p>
    <p>So we returned to our hotel faced with a days delay in our visa program. </p>
    <p>The next step in the overall visa process was to make an appointment with the Uzbek Embassy. We had arranged with
    Celestial Mountain Tours to make this appointment for us as such appointments can only be made in Russian. During the
    course of the afternoon we discovered that:</p>
    <ul>
    <li>appointments can only be made for 10:00am (which ruled out Wednesday)</li>
    <li>and the Uzbek Embassy was closed this coming Thursday</li>
    </ul>
    <p>Hence we would not be able to get to the Uzbek Embassy until Friday.</p>
