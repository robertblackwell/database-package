    <p>On Wednesday (31st) we presented ourselves at the Iranian Embassy at 9:30am in the hope that we could get our 
    passports (with the visas) early enough to then visit the Tajikistan Embassy before they stopped taking applications
    for the day at 11:00am. The Tajikistan Embasy website told us that between 9:00 and 11:00 they take applications and between
    11:00 and 13:00 they return issued visas.</p>
    <p>We sat around in the Iran Embassy waiting area for about 45 minutes and eventually, just before 10:30,
    we were called up to the service window and our passports with visas were returned. Success!!  </p>
    <p>Outside we found a taxi, showed the driver the address of the Tajik Embassy (in Russian) and we were on our way to our
    next stop. The Tajik Embassy is in one of the less propserous areas of Bishkek and for a little while
    we thought the taxi driver may have been lost but our concerns were misplaced and he dropped us right at the gate.</p>
    <p>It took us a few minutes to find the correct entrance gate to the embassy and then it took 10 minutes for some one
    to open the gate in response to us (and the gate guard) pressing the door bell button. Inside the embassy
    we quickly determined that visas were being issued on the spot. We waited around while a South African lady was served,
    were given and filled out applications forms, paid our money and within an hour we had both our Tajik visas and our GBAO passes
    that would allow us to travel in the <em>Gorno-Badakhshan Autonomous Oblast</em> region of Tajikistan.</p>
    <p>The South African lady (Isabel) is hoping to live in Dushanbe (Tajikistan) for a period and invited us to contact her
    when we get there. While we were waiting for our visas to be issued an American guy came in for a visa. We found
    out that he runs a green house in Utah for 6 months of the year and during the hot months in Utah (when green houses
    don't work) he travels. </p>
    <p>In the afternoon we ventured out into the heat again to do some shopping (a light weight scarf, blouse and makeup for Nina),
    visit a nearby coffee shop and generally have a bit of a look around.</p>
