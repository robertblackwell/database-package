<h2>Battery Management Strategy</h2>
<p>So given all that I (think I) have learned; how do I currently manage the camper battery pack.</p>
<ul>
<li><p>I try to bring the battery pack back to fully-charged by (sundown) at the end of each day.
If necessary I will run the generator to achieve this. </p></li>
<li><p>Days when we drive for even a few hours makes the previous goal easy to achieve.</p></li>
<li><p>Sunny days when we dry camp and don't drive (like Quartzsite in January) are easy to
manage and typically require less than 1 hours use of the generator.</p></li>
<li><p>Days when we dry camp, don't drive and the weather is cloudy are the most challenging.
On these days we try to limit usage and tolerate longer periods of generator usage.
In really challenging circumstances we sometimes let the battery pack stay below 100% SOC
for a few days. This works because we very rarely stay in the one pace for more than 2 or 3 days.</p></li>  
</ul>
