	    <p>This morning we felt a little conspicuous with our bright (though now dust covered) yellow truck
	    parked among the more dower working trucks so we decided to get out onto the road a little earlier.
	    Besides with the water system not working I cannot use the coffee maker (what a hardship).	
		A little way down the road we came upon one of the now familiar roadside parking spots. These
		are peculiar facilities. They are muddy patches of ground, sometimes with
		remnants of pavement, and covered in abandoned litter of kinds one does not
		even want to imagine. But that is not the peculiar bit. They each (of them we have seen anyway)
		have a large ramp made from slabs of concrete. It is like a low table with an on and off
		ramp. Even the roadside sign that designates the parking place has a picture of a car on the ramp.
		When we work out what the ramp is for we will let you all know.</p>
		<?php Skin::JournalGalleryThumbnails($trip, $entry); ?><p>Anyway at this particular parking place we decided to spend some more time trying to
		come to grips with our water problem. A couple of hours playing around with the
		under sink plumbing has achieved a workable but far from ideal camper water system. I
		have succeeded in plugging the pressure relief value so that we no longer have a gush of
		water when the pump is turned on. But now when the pump is turned on pressure builds in
		the hot water tank but the pump does not turn itself off. Indeed I have heard popping noises
		from the hot water tank as the water pressure stresses it. So for the moment
		we have adopted a practice of leaving the pump off and one of the hot water faucets open
		so that pressure in the hot water tank does not become excessive. As a further
		safeguard I have turned off the heat to the hot water system. When cold water is required 
		we simply place a container under one of the faucets, open it and turn on the pump.   </p>
	    <p>Thanks to the wonders of modern communications I have already sent an SMS to Roger at
	    GXV about the pressure relief value. Over the next couple of days we will probably investigate
	    having a new valve and pump shipped to us.</p>
	    <p>We decided on an early stop today at yet another one of those signposted parking places.
	    Throughout the afternoon various trucks have stopped and then proceeded. But the place seems busy 
	    enough to be safe.</p>
