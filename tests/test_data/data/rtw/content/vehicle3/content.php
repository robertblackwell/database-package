<h3>Our particular U500</h3>
		<p>The particular vehicle we acquired has the 8-forward 6-reverse speed 
		Telligent manual transmission, plus both <em>"working gears"</em> (thats low range)
		and <em>"crawler gears"</em> (that's super low range).</p>
		<p> I got a real kick from the
		spec sheet. Top speed in the crawler gears (with the
		transmission in 8th or top mind you) is <em>15 kph</em>. I am looking forward to
		trying out those crawler gears to see just how slow they are.  </p>
		<p>Our vehicle also has the up rated engine (286HP), an uprated chassis at 33,000 GVW
		and 395/85R20 tires. It does not have CTIS (central tire inflation system) and I am
		probably a little disappointed about that.</p>
		<p>Oh, and by the way the truck is 
		<em style="color:yellow; background-color:black;font-weight:bold;padding:0px 5px 0px 5px">YELLOW</em>, 
		the camper is also. We will look a bit like a bumble bee coming down the road. </p>
        <h3>Modifications</h3>
        <p>We commissioned the following modifications to the truck.</p>
        <ul>
<li><p>The wheelbase has been extended from 153" to 181" to accommodate
            the camper box without an exceptionally large overhang or steeply sloping
            rear. This required an extended rear driveshaft and intermediate bearing block.</p></li>
            <li><p>An additional fuel tank with a capacity of 100 gallons has been added
            giving a total fuel load of 160 gallons. I am hoping to get the range up
            towards 1,500 miles.</p></li>
            <li><p>A Webasco 90 ST hydronic heater has been installed. This provides hot water 
            to the camper while the truck is running and can also heat the truck coolant when
            the truck is not running. The camper heating is derived from hot water, but more on
            that later.</p></li>
            <li><p>A sliding metal door was put in the back of the truck cab to facilitate entry into
            the camper from the driving position.</p></li>
            <li><p>We added a RedDot roof mounted air conditioner to the truck cab
            as we had doubts about the effectiveness of the Unimog stock A/C in extreme
            conditions. The vario-pilot takes up some ducting space and seems
            to have the side effect of reducing the effectiveness of the stock AC.</p></li>
            <li><p>Finally, the three point mounting system that connects the camper
            to the truck had to be installed. Is this a truck mod ?.</p></li>
        </ul>
<?php RenderArticleGallery($trip, $slug, "truck-mods");?><h3>A winch - the mod we did not (yet) make</h3>
        <p>It probably seems strange to many that we did not (or maybe it is "have not yet") fitted a winch
        to our Unimog. After all this vehicle is such a 4x4 icon it is almost sacrilege not to put a winch on it,
        even if only for the "look". To top it off Warn even makes a hydraulic winch
        specifically for the Unimog.</p>
        <p>My reasoning is simple. The Warn Unimog winch is about $5000, and has a pull rating of
        18,500lbs. The vehicle will be about 24,000 lbs all up. All the recovery experts tell me
        that the winch should have a pull 2x to 3x the weight of the vehicle. Thus 
        on that basis I need a 40,000 - 60,000lb rated winch. Such a winch will cost a lot
        more that $5000 and will weigh hundreds of pounds. Thus I will wait and see
        if a winch is needed - after all I plan on <em>overlanding</em> not <em>rock crawling</em>. </p>
