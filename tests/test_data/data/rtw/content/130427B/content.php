    <p>After an interesting and amazing journey across the Lena River by hovercraft we were met by a friend of Alexander's
    in a UAZ pickup (this is a small modern pickup truck built by the UAZ company) and taken to look at an apartment that
    Alexander had organized for us to rent (what we would have done with out Alexander I have no idea). After
    some paper work and payment we were left for a couple of hours to rest and cleanup, then it was off to do some sightseeing.</p>
    <p>After our rest Alexander returned with two of his younger relatives, Kirill (a young man) and Inna (a young lady).
    Our two younger hosts had the arduous job of being translators. Over the next few days we would become very … very
    appreciative of their English language skills. </p>
    <p>So off we went to the <em>Permafrost Institute</em>. This is perhaps Yakutsk's most famous tourist attraction
    because it is unique. The study of permafrost in Yakutsk started in the 1800's when a merchant commissioned the digging
    of a well. What he discovered to his financial disadvantage is that a few feet below the surface is frozen soil
    and it continues for somewhere between 100 and 300 meters. The well never produced water but it did give
    scientists a chance to research permafrost and get the first measurements of how far down the frozen soil
    went. From this beginning an institute was established to study permafrost and in particular how to adapt building
    practices to meet the unique problems presented by permafrost.</p>
    <?php RenderPostGallery($trip, $slug, "picts");?><p>A tour of the institute involves going underground into the permafrost layer where the temperature is -10°C
    year round and looking at some mammoth bones, the frozen soil and the ice crystals that grow on the walls
    and ceiling of the underground walkways. It was really interesting.</p>
    <p>One of the results of research undertaken at the institute is a building practice that is seen all over
    Yakutsk. All of the large modern buildings like apartments and office blocks have an air space between the
    building and the ground and the building itself sits on top of a series of pillars. This is to allow
    the cold winter temperatures to touch the ground surface. In earlier times when buildings were placed directly
    on the ground the underlying permafrost would melt and the building would gradually sink into the ground.</p>
    <p>The day was capped by an invitation to Alexander's apartment for dinner and a chance to meet his wife Maria.
    With the help of our two interpreters we had a very sociable dinner exchanging information about ourselves
    and getting to try some more Yakutian food. Unfortunately I don't think Nina and I will acquire the
    taste for <em>blood sausage</em>(a mixture of blood and milk encased in an animals intestine)
    anytime soon but all the other food was delicious.</p>
    <p>Visiting Alexander's apartment and asking for his address brought home to us an  interesting feature
    of Russian apartment buildings that we have seen in other cities. There is no foyer or main entrance.
    Each building has a series of external doors each of which opens into a stairwell and from which
    the apartments off that stairwell are accessed. </p>

