<div id="main_content">
    <p>We chose to have GXV build and mount a Safari version of their camper
    box. You can get the general dimensions and layout of this box from
    the drawings in the images below (just click the thumbnail to get a larger view).</p>
    <p>In broad strokes the camper box (not the overall vehicle) is 15.5 ft long, 8 ft wide and just shy of 8 ft high. The internal
    layout is pretty much a GXV standard arrangement.</p>
    <p>With those camper dimensions
    the overall vehicle will be (understand that at the time of writing it is not complete)
     about 26' long, 8' wide and 12'10" high.</p>

    <?php RenderArticleGallery($trip, $slug, "drawings");?><h3>Our choices</h3>
    <p>The key choices that we made for the camper are:</p>
    <ul>
<?php RenderArticleGallery($trip, $slug, "camper-choices");?><li><p>The step down door. We decided to have a drop down for the camper door
        with an electric auto opening step to make entry easier. I regretted
        using the chassis space and the perceived reduction in clearance, but my wife at 5'1"
        would find a door 4' off the ground too hard to manage.</p></li>
        <li><p>The square tail on the camper. Many expedition trucks have a sloping
        or angled tail on the camper to provide a better departure angle and aid in
        exiting from ditches and gullies. We decided early on that we would sacrifice
        that additional maneuverability in favor of greater internal storage. 
        We regained some of our departure angle by having the truck wheelbase extended.</p></li>
        <li>
<p>An Onan Q3200 diesel generator mounted inside (but isolated from) the rear storage area.</p>
        <p>This is really three decisions. </p>
        <ul>
<li><p>The first decision was whether to have a generator. That we would have one and that it would be 
            diesel was never in doubt. Boondocking in hot weather without A/C is just not my idea of fun.</p></li>
            <li><p>The second decision was to use an Onan. GXV has typically used Fischer-Panada generators
            in their builds, these are sophisticated liquid cooled units that seem to be used 
            a lot in small yachts. I preferred to go with something simpler. </p></li>
            <li><p>Mounting the generator inside the camper body. GXV typical mounts the generator
            on the chassis below the camper body. I preferred to have the chassis space available
            for an additional fuel tank and to keep the generator up away from any water for
            what we hope will be infrequent water crossings.</p></li>     
        </ul>
</li>
        <li>
        <p>A pass-thru between the truck and the camper. This was also never in doubt.
        In our opinion this feature is a major convenience/requirement when camping in
        bad weather or in urban locations.</p>
        </li>
        <li>
        <p>Roof rails on the camper body. These are metal tubular rails along the leading 
        and upper edges of the camper to protect the camper body somewhat when we inevitably run
        into some substantial tree branches.</p>
        </li>
        <li>
        <p>A large rear carrying rack that can accommodate more than just the spare tire
        and its associated small winch. Seems like a good place to carry fire wood, maybe additional fuel
        and nomadic hitch hikers.</p>
        </li>
        <li>
        <p>Glass not acrylic windows. We had the experience in the Earthroamer of scratching the
        window panes on tree branches while driving on forest trails; did not want a repeat. </p>
        <p>Also the windows we chose have provision for metal security inserts to make the windows
        proof against break-in.</p>
</li>
    </ul>
