<h3>Camper equipment and fittings</h3>
    <p>All the other stuff that goes into making a camper is pretty much standard GXV.</p>
    <dl>
<dt>Hotwater</dt>
    	<dd>
<p>Hot water is provided from the chassis mounted Webasco 90 ST hydroponic heater. This unit
    	is either diesel fired or heated by the engine coolant. It feeds a hot water <em>Isotherm</em> storage tank
    	under the sink area.</p>
    	</dd>
    	<dt>Heating</dt>
    	<dd>
<p>A hot water radiator and fan unit mounted under the dinette is the primary source of camper heating.
    	The fan in this unit forces air over the hot water radiator and circulates the air around the camper. </p>
    	<p>A small electric heater used in conjunction with the generator is the backup heat source.</p>
    	<p>There is no heated air system as found in a typical RV.</p>
    	</dd>
    	<dt>Cooling</dt>
    	<dd>
    	<p>A 13,500 btu roof A/C unit is included. It requires the generator to be running or shore power.</p>
</dd>
    	<dt>Refrigerator</dt>
    	<dd><p>The Vitrifrigo refrigerator is somewhat unusual for an RV as it has two drawers
    	rather than doors. The top drawer is a freezer compartment and the lower
    	drawer is the refrigerator compartment. It operates from the 12Volt
    	electrical supply.</p></dd>
    	<dt>Electrical</dt>
    	<dd>
<p>The electrical power system is quite something.</p>
    		<ul>
<li><p>The heart of the system is a battery pack consisting 
    			            of 3 x 250 AH glass mat deep cycle batteries.</p></li>
    			<li><p>The batteries feed into a 3.6 kw inverter that is part of an Outback Inverter/Charger.</p></li> 
    			<li>
<p>The batteries are fed by</p> 
    				<ul>
<li><p>2 x 180 watt solar panels,</p></li>
    					<li><p>the truck alternator while on the road</p></li>
    					<li><p>the generator when it is running</p></li>
    					<li><p> shore power of 110V/60Hz via the charger part of the inverter/charger</p></li>
    					<li><p>230V/50Hz (or any other voltage) shore 
    					power through a universal/world charger.</p></li> 
    				</ul>
</li>	
    			<li><p>All ac appliances operate on 110V/60Hz and are fed by the inverter, or by a direct shore power feed
    			when shore power is 110V/60Hz.</p></li>
    			<li><p>The battery pack is wired into the truck 
    			starting system and can be used to start the truck engine in an emergency.</p></li>
    	    </ul>
</dd>
    	<dt>Lighting</dt>
    	<dd><p>All lighting is LED.</p></dd>
    	<dt>Bathroom</dt>
    	<dd>
    	<p>The toilet is a 5 gallon canister type. Great for boondocking and operating in environments that
    	don't have typical RV park hookups.</p>
    	<p>The shower and kitchen sink empty into a 25 gallon gray water tank.</p>
    	</dd>
    	<dt>Entertainment</dt>
    	<dd>
            <p>Entertainment equipment is relatively sparse. The camper has a radio/cd player and the truck has a 
            satellite capable radio with CD player and iPod connection.</p>
            <p>These days almost all of our entertainment comes through our iPods, Kindles or MacBooks.</p>
    	</dd>
    	<dt>Finishes and Furnishings</dt>
    	<dd><p>The finishes are nice but simple. The bathroom cabinets and shower grate are teak. 
    	All other cabinets are alder. The floor is covered in a woven nylon that is easy to clean and 
    	sturdy. Counter tops are granite. The walls retain the white gel coat finish of the composite
    	panels that the camper shell is made from. </p></dd>
    </dl>
