<h2>Amp Hour (AH) Usage and Budgeting</h2>
	<p>In the previous posts we looked at the capacity or rating of the various chargers and components in the camper‘s
	electrical system and concluded that <em>possibly</em> the system has too little charging capacity given the
	size of the battery pack.</p>
	<p>How does the system work in practice, given actual usage?</p>
	<h3>Use the generator for high draw appliances</h3>
	<p>The very first lesson we learned once the FLEXNet was installed was that the microwave oven (particularly in convection mode),
	the toaster and the coffee maker apply high current loads to the batteries through the inverter. For short duration use
	the net AH consumption is small but the convection oven in particular can easily consume over 100AH in a single use if
	the cook is baking something elaborate. The lesson - whenever possible run the generator to power the oven, toaster and
	coffee maker.</p>
	<h3>Solar only makes a modest contribution</h3>
	<p>The second lesson was that the solar panels only make a modest contribution. During the summer we were driving across Canada
	in generally sunny weather. Though Canada does not have what you would call direct overhead sun. We rarely saw more than
	100AH per day from the solar panels and charger, and the very best day was 130 AH. Days of cloudy or rainy weather dropped that
	back to 30AH. Later in the year (Oct, Nov, Dec) we were in the South West (Arizona, New Mexico) with lots of sun but,
	less hours of daylight and at a lower angle. During this period the solar system delivered only around 80 AH per day. </p>
	<h3>The refrigerator is a substantial consumer</h3>
	<p>The refrigerator draws 5-6 amps when the compressor is running. During hot weather it can run for up to 20 hours per day
	and hence consume as much as 120AH per day. Cooler weather reduces this consumption but not by as much as one might expect
	as the interior of the camper easily maintains a 70+°F temperature. </p>
	<h3>Usage: 100-150AH a day</h3> 
	<p>By using the generator for high draw appliances we have come to expect a daily usage of 100-150AH. That is up to 20%
	of the battery bank capacity.</p>
	<h3>Replenishing the batteries</h3>
	<h4>While on the move</h4>
	<p>We have been able to replace that daily usage when we have a good sunny day combined with 4-5 hours of driving. The
	contribution from solar, a contribution from the alternator (without the diode isolator getting in the way) and a 
	modest (up to 30 minutes) use of the generator will typically bring the batteries back to full charge. </p>
	<h4>While NOT on the move</h4>
	<p>On a sunny day without driving we can typically regain 50-60AH bringing the daily deficit into the range of 70-100AH. 
	Which gives us an endurance of around 3-4 days without lengthy periods of generator use. This is based
	on a maximum discharge of 330AH (50% of the battery pack capacity) </p>
	<h4>Recovery from deep discharge</h4>
	<p>To date we have not at any time drawn the batteries down by 330AH or to 50%. Hence we have no experience of
	recharging from such a discharge. However we estimate that recovery from such a state of discharge would require the
	Outback Inverter/Charger to run for 8+ hours. This effectively means we would need a night in a campground with 110v/60Hz
	power or we would need to run the generator for 8 hours. I am are skeptical that solar, the alternator, nor the ChargeMaster (world charger)
	could, in any combination, recover the batteries (and continue to run the camper) from such a deep discharge 
	in any reasonable time frame.</p>
	<h3>Shallow discharge, charge capacity</h3>
	<p>To date we have used our camper in a manner that recovers the batteries from a typical 100-150AH (20% of capacity) deficit each day.
	As a result of this practice the various chargers we have on board the vehicle do not spend much time in the bulk phase
	of the charging cycle, where their maximum current output can be utilized, 
	but almost immediately go into the absorption phase. Consequently we have not
	felt the effect of any apparent lack of capacity in the Outback Inverter/Charger.</p>
	<p>Because all travel to date has been in the US/Canada we have little experience with the ChargeMaster world charger.</p>
	<h3>The "last" 100 AH</h3>
	<p>Putting that last 100 AH back into the batteries (from 85% charged to 100% charged) is slow because at
	this point in the recharge cycle the chargers are in their absorption phase and charging current levels
	are down to 20-30 AMPs. This phase can easily take 4 hours. It is in this phase that the solar system
	and the alternator (via the Sterling Battery-Battery Charger) come to the party - those devices
	can complete the "top up" of the batteries while driving on a sunny day. But without the combination
	of sun and driving, the top up would require 4 hours of generator or shore power.   </p>
	<h3>Inefficient use of the generator</h3>
	<p>It has taken me a long time to come to the following realization. </p>
	<p>Our 3000 watt generator is much bigger than is required for recharging the house batteries. 
	Even when the Outback charger is putting our 100 amps of charging current it is consuming
	less than 2000 watts of AC power. Thus if battery recharging was the only mission for our
	generator we could easily get by with one of those little 2000 watt Honda models.  
	And certainly a smaller quieter generator would be very convenient for replacing those
	last 100 AH because during that phase less than 1000 watts of AC power is required.</p>
	<p>I will have more to say about generator choice in a future post, but for the moment
	let us complete this discussion by noting that the generator has been sized to meet
	the power requirements of the camper A/C unit NOT to meet peak battery recharging needs.</p>


