		<p>Sergie joined us just before lunch and we piled into the truck for a short drive north to a facility
		that Nina and I would call a zoo (its name is Blagodarim - Благодарим). We spent a hour or more
		photographing the range of creatures on display. The highlights were Siberian Tigers,
		Russian bears (including a couple of cubs), a very rare Leopard from the region (only 30 left),
		Lynx, wolves and a polar bear from Canada. Sergie told us that the place ran on a very tight budget
		but even so the animals and their pens are in good condition. The paths for the human visitors were
		in a slightly poorer state of repair partly because there has been little repair from the
		damage of winter.</p>
		<?php Skin::JournalGalleryThumbnails($trip, $entry); ?><p>We drove a few hundred yards further, down to the gravel shore of the Amur River
		where we took some photos of us and the truck next to the still frozen water.</p>
		<p>After dropping the truck back at the hotel we caught a taxi into the city center to a museum about
		the Khabarovsk region where we met our guide Irina from yesterday. She did a heroic job of giving us a tour
		and with out any language in common managed to convey a lot of information. It really is quite a good museum.
		We saw many of the animals from this morning though now stuffed, but the highlight for me
		was a series of galleries that depicted life in Khabarovsk at different periods. We learned for example
		that in Soviet times Khabarovsk was a manufacturing center for military aircraft and continues that
		tradition today but now makes civilian planes. </p>
		<p>Organizing a taxi back to our hotel was a bit of a challenge, but one we overcame with help
		from the museum staff. It seems that cell phones have changed the process of getting a taxi for 
		non Russian speaking tourists.
		No longer does one walk outside and wave at passing taxis and then show them a hotel business card.
		Now one must call the taxi dispatcher on a cell phone and give pickup and destination. Within a few
		minutes an SMS message arrives with a description of the car (many taxis are not marked) and the
		price of the ride.</p>
		<p>We finished the day with an evening visit to Sergie's apartment where we had a few drinks
		while looking at some of Sergie's photographic works; he is a keen and accomplished semi-professional
		photographer. The better photos of the animals in the above set where taken by Sergie (full name
		Sergey Makogonov) and he holds the copyright to the images. They are displayed here with his permission.</p>	
