<h2>A Bigger Universal Charger</h2>
<p>I have agonized for sometime over what to do so that our GXV camper can make effective use of 230V/50Hz
shore power. Specifically this question focused on replacing or supplementing the inadequate Mastervolt 12/35
charger that came as original equipment in the camper.</p> 
<p>I had been leaning towards installing a pair of Xantrex TrueCharge2 60Amp chargers and making use
of their paralleling feature to get an effective 120amps of charging power regardless of the country or voltage
of the shore power supply.</p>
<p>However, at the end of the day I chose to go with a Mastervolt Charge Master 12/100 device.</p>
<p>I found some bad reviews of the TrueCharge2 on the Westmarine website that indicated that at least early
versions of the product failed soon after installation. Given that I and the vehicle were scheduled to be in Russia
a few weeks after a proposed purchase this information suggested a TrueCharge2 might be a risky choice.</p>
<p>The second factor was space in the campers electrical cabinet (under one dinette seat). After adding 2
Sterling-Power B2B devices to the original equipment that space was getting crowded and the Xantrex solution
would require finding a way of squeezing in two more devices. On the other hand the Mastervolt solution was
only a single device.</p>
<p>The down side of the Mastervolt choice is firstly cost; that single device is more costly than two TrueCharge2
and their associated remote control. Further without a Masterbus remote control unit (another $400-$500) the
ChargeMaster cannot be instructed to limit the current it draws from the shore supply, a feature that the Xantrex 
solution would have provided. </p>
<p>Thus the decision to buy a ChargeMaster was a compromise</p>   
