		<p>This morning our guardian angels Elena (in Vladivostok) and Irina (in Khabarovsk) succeeded in getting 
		that document from the hotel into our hot little hands. The back story to the document's delivery is interesting.
		Last night around 9:00pm a train called the <em>Ocean</em> departed from Vladivostok headed for Khabarovsk. 
		Aboard the train was Irina's doctor (friend or family doctor we never did work out). Elena gave him the 
		document and he carried it for the overnight train journey. Irina met the train, took delivery of the document 
		and then dropped by our hotel to give it to us.  </p>
		<?php Skin::JournalGalleryThumbnails($trip, $entry); ?><p>We spent much of the day doing chores including buying a new phone chip (it seems we need a different telephone company
		for the road to Yakutsk). Also a visit to a local hardware store in search of screws and some light nylon cord to try 
		and stabilize a couple of the drawers that don't like the rough roads.</p>
		<p>Irina (one of the angels) picked us up at 5:00pm and we accompanied her while she picked up her two sons'
		first the youngest from a kindergarten and then her eldest from his elementary school. It was interesting to see 
		the schools. Older buildings and not much to look at on the outside but inside well appointed with a good deal
		of equipment and facilities. The elementary school is a government school (we would say state) but the kindergarten
		is what Irina called a "railway school"; meaning it is run by the Russian railway company for the children
		of railway workers. It turns out that Irina's husband is a surgeon in a "railway hospital" (in Vladivostok)
		and is hence a "railway worker". </p>
		<p>After the parental pickup duties we joined the family at their very comfortable downtown apartment for dinner.
		It was a trip down memory lane for Nina and I watching Irina juggle dinner preparation, assorted phone calls,
		guests and 2 very energetic boys.</p>
		<p>When we arrived back at the truck and while Nina was giving Irina and her boys a quick tour the son
		of the owner of our hotel knocked on the door offering us a "fill-up" with water. In addition Nicholas
		gave us some bottled water from his fathers factory and snapped a few pictures of the truck. </p>
		<p>One of the more amusing events of the day was the taking of our morning medications. Both Nina and I 
		take a number of tablets each morning (one of the perks of age) and Nina always has trouble shallowing
		the larger of her ration. This morning she added an additional hurdle to the process by taking a
		large swig from a coke bottle of clear liquid that she thought was fresh water but turned out to be vodka.
		Why was there vodka in a coke bottle? The previous night we purchased a small bottle of vodka but once open the
		cap would not reseal so we decanted the remainder into a coke bottle. </p>
