		<p>Along the road from Vladivostok we have often seen people collecting water at community wells or community
		faucets; but as we approached Khabarovsk we saw a spectacular example of this practice. A group of people
		had congregated around a huge ice mound in the middle of which was a flowing water pipe. With large water
		bottles in hand people where trying to climb the ice mound to get their bottles filled at the flowing pipe.
		Nina got out and took a picture or two. She was not sure whether her presence, and the camera, was welcome
		or not as no one acknowledged her presence.</p>
		<p>After a quick stop and single picture at the monument marking the start of the city we set about navigating 
		our way to a hotel (the Guru) we had selected for our stay in Khabarovsk. We had decided on a hotel in order to 
		get internet and a shower (given the problems with the camper water system) in addition to parking. Khabarovsk is a large
		and complicated city but with the aid of our new Garmin GPS (a Montana) and digital maps of
		Russia downloaded from the Open Street Map project we found our way with little problem.</p>
	<div id="camping">
		<p>While the navigation might have been without a problem the checkin at the hotel was not. To cut a long
		story short it turns out that we had neglected to finalize the registration of our visas when we left the hotel 
		in Vladivostok and as a result the Guru could not allow us to register as guests. Fortunately they directed us
		to a near by hotel (the Abrikol) that does not require visa registration. While we have no idea
		why some hotels require registration and some do not we were pleased to have a place to shower and connect.
		It is worth noting that we would not have managed our way through this complex situation without the help
		of Sergey a contact name we had been given by Sallie in La Junta, CO. as no one at either hotel spoke any English.
		There is more to come on this topic as while we might have a hotel for a night or two we are
		now technically illegal and may have problems exiting Russia. </p>
	</div>
		<p>Not to be put off by a simple immigration problem we went for a tour of the downtown section of the city 
		with Sergey after we had checked in. From the top of the main street we got a good view of the Amur River,
		and just across it's wide expanse of ice (yes it is still frozen) a glimpse of China. We saw
		a number of churches with their characteristic onion domes (cupolas) and a large war memorial. </p>
		<p>Khabarovsk is the governmental center for the whole of Far Eastern Russia (an area over
		half the size of the United States) and as a result a lot of Federal funds are being invested 
		to make the place a show case. Sergey named a large number of buildings that had either been
		substantially renovated or newly constructed since 2000 and as a result the city has 
		(and is being) transformed.</p>
