		<p>Today we discovered that we had made a really dumb mistake when we left our hotel in Vladivostok, but I will
		tell you about that below. We also found and fixed another apparent problem with the camper. Best of all
		with the aid of our digital map from the <a href="http://openstreetmap.com">Open Street Map project</a>
		we navigated our way through the city of Khabarovsk to a hotel.</p>
		<p>On waking this morning I noticed that the heater had not fired up during the night. This was unexpected and unwelcome
		as it had been a somewhat cold night; below freezing and with some light snow. I also determined after
		a little experimentation that the engine block heater also would not fire and moreover gave
		no error codes to hint at the cause of the problem. However the generator started after
		a few tries and we managed to heat the camper with a small electric radiator. So with this perplexing problem
		hanging over our heads we started driving with the goal of being in Khabarovsk early afternoon. </p>
		<p>An hour down the road I decided to act on a theory proposed by Nina regarding the heater problem.
		She reasoned that the only thing we had done recently that could (in her mind) impact the heater
		was to try and prevent hot coolant from entering the hot water tanks heat exchanger and hence that was
		possibly the problem. Well it took all of two minutes to open the faucets that supply hot coolant to
		the hot water tank and almost immediately the heater (both camper and engine block) were functioning again.
		Put one down to Nina </p>
