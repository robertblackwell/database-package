		<p>It seemed to take forever to get on the move today. What with making the last use of our hotel room,
		catching up on emails, talking (Skype) to family it was midday before we started moving; and then only to 
		a local super market to top up our larder.</p>	
		<?php Skin::JournalGalleryThumbnails($trip, $entry); ?><p>The route west from Khabarovsk crosses the mighty Amur River (the 10th longest in the world) on a long
		and very sturdy looking bridge. From the bridge we could see that the river is still mostly ice.</p>
		<p>Our goal for the day (afternoon really) was to get as far as the town of Birbidzhan (Биробиджан).
		This town is the capital of the "Jewish Autonomous Region" a region established by the Soviets in 1934
		as a place for Jews to call their own, and indeed for a few years Jews came from all over the world
		to live here. But it did not last and today the Jewish population numbers only a few thousand.
		However the historical intent is evident as the sign at the entrance to the town declares
		the towns name in both Russian and Hebrew. Finally it is worth mentioning that
		the towns name derives from the fact that is sits at the junction of the <em>Bira</em> and <em>Bidzhan</em>
		rivers.</p>
		<p>We spent no time looking around the town simply took the bypass from the highway through the center of town
		and only saw what was visible from our moving truck. For me this meant I did not see much beyond traffic, 
		potholes, street signs and my GPS. After this experience Nina and I agreed that in future we need to decide
		<em>before</em> we enter a town what our plans are; to park and look or simply pass through. 
		The challenge of navigating through the town was made easier by thoughtfully provided signs indicating
		the truck route; and the fact that the route and streets seemed to correspond to the map
		I have in our GPS.</p>
	<div id="camping">
		<br><p>We continued west after Birobidzhan along the bypass until it rejoined the highway and then (a little east of the junction)
		found a <em>кафе</em> (and truck stop) across the road from a gas station to spend the night.</p>
		<br>
</div>
		<p>In the early evening one of those 6 wheel drive buses turned up to collect a load of
		engineering parts from a guy in a small pickup truck and I got a chance to have a closer look.
		The bus cabin is a metal framed/metal skinned structure that is simply bolted to the truck
		chassis (no fancy three point mounting system here). The truck has plain vanilla leaf springs 
		and sports 425/85R21 Russian made tires with a  pattern that is reminiscent of Michelin XZLs.</p>
		<p>On the topic of vehicles, this part of Russia seems to be left-hand-drive roads and right-hand-drive
		vehicles. We are driving on the right hand side of the road (the American side) yet the vast majority
		of light vehicles (cars and small to medium trucks) have the steering wheel on the right hand side (the British
		and Japanese side). The only category of vehicle which are predominately left-hand-drive
		are tractor trailers (semi-trailers to Aussies) and 18-wheeler to Americas; except that
		they typical don't have 18 wheels here as the trailers seem most often to
		have three axles at the rear with only 2 wheels per axle and often even the tractor (the truck part)
		does not have duallies. </p>
		<p>We have also seen a lot of all-wheel-drive heavy trucks. Such things are a rare sight in the US but here
		they are quite common. I have even seen a number of all-wheel-drive tractor trailers.</p>
		<p>Finally today we saw one of those concrete ramps at a roadside parking place put to use. A guy
		with a blue <em>Russian Jeep</em> was doing some repairs/maintenance on his vehicle using the ramp.
		Guess that explains them - also says something about the roads I think. </p>
