<h3>Shipping our truck to Russia</h3>
<p>We had a good experience shipping our expedition truck (motor home) from the US port of 
Everett to the Russian port of Vladivostok. But this did not come about without a good bit
of research and probably a good bit of luck.</p>
<p>Without a doubt the most important ingredients in our good experience were the agents we used
at each end of the shipping process.</p>
<p>The shipping company was FESCO who bought space on a RORO vessel (in our case the Grand Pace)
owned by some other company (I never did remember their name).
</p>
