Interestingly their main line of business is shipping personal belongings for 
people moving into and out of Russia on work assignments.</p>
<h3>Costs</h3>
<p>Our vehicle is big, 3.9m H x 7.8 m L x 2.7m W and weighing in at  10,800KG. </p>
<pre>
    US PacTerminals             USD 150     (driver to take vehicle to port - special security clearance required)
    US PacTerminals             USD  75     (customs clearance)
    Shipping costs              USD 8550
    Links Ltd                   USD 200     (acting as agent for us)
    Vladivostok Port Fees       USD 322
</pre>
<p>The only elements of this overall cost that could in any way be considered optional (and avoided by doing
some of the work oneself) is the $75 for US customs clearance and $200 to Links Ltd for their assistance
in Vladivostok.</p> 
<p>However in my opinion these costs are remarkably low for the services provided.
I will say more about the US Customs clearance below. As for Links Ltd not only did they prepare a
thick bundle of required documents in Russian they also transported us to the port and FESCO offices
a number of times as the vehicle owner is required to present themselves and sign various papers.</p>
<h3>The process at the Russian end</h3>
<p>We arrived in Vladivostok on Thursday April 4th. We knew from an internet search that the vessel transporting our
truck (Grand Pace) docked the same day.</p> 
<p>Friday 5th April we met Svetlana (a rep from Links Ltd) at our hotel at 3:00pm and gave her copies of passports, visas,
vehicle title and other info which she turned into various documents over the weekend. Later that same day while
sightseeing we observed the Grand Pace moored and our vehicle parked on the dock beside the vessel. Thus we knew
the vehicle had been unloaded.</p>
<p>Monday 8th April we met Svetlana and an insurance agent (arranged by Links Ltd) at our hotel at 9:00am,
paid for 3 months Russian vehicle insurance (needed for port processing), signed some more papers,
and then went with Svetlana to the port where we in turn visited FESCO's office and customs. 
We were done by about 11:00 and Svetlana dropped us back to our hotel. During the day we understand that
Yuri (Svetlana's boss) had to do something at the port related to our vehicle. At 4:30 Yuri took us back to
FESCO's office for some more paperwork. Later in the evening we heard from Yuri that all the paperwork for
that day was done and that we would get the truck the next day. </p>
<p>Tuesday 9th April Svetlana picked us up at 9:00; one more visit to FESCO, then we waited at the port gate 
for Yuri to drive our truck from the port (foreign nationals are not permitted to enter the port). 
By about 10:30 all was done.</p>
<p>Amazingly straight forward.</p>
<h3>Process at the US End - Personal belongings</h3>
<p>We started investigating how to ship our truck in early 2012 and at that time the RORO service
to Vladivostok departed from the port of Tacoma. By the time we got to March 2013 there had been some
kind of major change in the world of RORO ships and the service now departed from
the port of Everett. This necessitated us finding a new freight forwarding company. With hindsight 
both the change to Everett and the selection of PacTerminals as our new freight forwarder
was a blessing.</p>
<p><em>There can be no personal belongings of any kind left in the vehicle. All items accompanying
the vehicle must be packed into a separate crate that will travel with/beside the vehicle.</em>. 
This was THE BIG PROBLEM at the US end. We were told this by various shipping companies and agents 
in both 2012 and 2013. We had also talked to travelers from Britain who shipped into Halifax and indeed did pack all their "stuff" into a crate.</p>
<p>As anyone traveling by expedition vehicle will understand this is a real issue; how does one transport,
cooking equipment, extra cloths, spare parts, tools ... the list goes on.</p>
<p>Just to make this a more thorny issue Yuri Melnik (at the Russian end) advised us to place
all of our belongings and equipment inside the vehicle; "Russian customs are experienced with travelers'
vehicles and understand they have stuff inside. Putting stuff in a separate crate will just cause problems". 
... Great!! </p>
<p>Fortunately for us PacTerminals and I came up with a game plan to get past this difficulty. Our camper has a
very large storage area under the bed that is only accessible through external doors. Also while our vehicle
has a pass-thru between the truck driving cabin and the camper we are able to close off and lock that pass through
and hence isolate the truck from the camper. Finally the key to the truck does NOT unlock the camper.</p>
<p>Thus the plan was as follows:</p>
<ul>
<li><p>Place all of our belongings and equipment inside the under bed storage area, so that anyone inspecting
the inside of the camper would find all cabinets and storage areas empty. The hope was that this would
allow us to pass a casual inspection.</p></li>
<li><p>Deliver the vehicle to the port with only the truck key. An arrangement was made by Pacterminals with
<em>the</em> customs inspector (there is only one) at Everett that if he wanted to inspect the inside of our
camper he would call and someone from PacTerminals would deliver the key and also attend the inspection.</p></li>
<li><p>As it turned out the camper was not inspected.</p></li>
</ul>
<p>It is worth noting that this arrangement worked because of PacTerminals willingness to "find" a solution,
their good working relationship with the customs inspector, and probably because Everett is a less hectic port.</p>
<h3>Process at the US End - a couple more things</h3>
<p>It seems that US ports require vehicles to be delivered a minimum of 7 days before departure
to provide adequate time for customs inspection. In our case we delivered the vehicle on March 11th
and the vessel sailed March 20th. For us this was no problem as we had a place to live while
the vehicle sat on the dock.</p>
<p>In addition a vehicle can only be delivered to the port by someone holding a <em>TWIK</em>
pass - some kind of special security pass. </p>
