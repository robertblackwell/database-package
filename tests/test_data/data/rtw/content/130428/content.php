    <p>After the business of making plans this morning we used the afternoon to do some tourist type things. In particular
    a visit to a local Regional Museum (we have seen one of these in every city we have
    visited and they are surprisingly well done). There is understandably a lot of overlap between all the regional
    museums as the big national and global issues of the last 150 years have effected each region in a similar manner
    (the revolution and WWII being the biggies) but they each also display history of the local inhabitants. So at
    this museum the interest was on the Yakutia people. An asiatic looking race who came to this area about 1000 years
    ago bringing with them horses and a way of life that seems (at least superficially) to have strong links
    to the Mongols. Today the Yakutia people number a little over 900,000 and form a politically semi-autonomous 
    group called the Sakha Republic. They occupy a region of north central Siberia about 2/3 the size
    of the lower 48 states of the US with Yakutsk as the capital. Based on the people we talked to, and various
    buildings and monuments around the town, the Yakutia people seem to have a very strong sense of identity
    and strong connection to their history.</p>

