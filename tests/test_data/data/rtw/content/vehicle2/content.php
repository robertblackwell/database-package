		<h3>Maintenance and Repairs</h3>
		<p>The topic that got me started down the path of "another vehicle" was
		the question of maintenance and repairs.
		Our Tiger-Chevy Duramax was incredibly reliable in South America, <em>BUT</em>
		it was clear from conversations with Chevy dealers in various countries
		that repairs on the engine/transmission would have been very difficult. Those vehicles,
		the Duramax engine and Allison transmission are not used in any commonly available
		vehicles in those parts of the world.</p>
		<p>The situation seemed no better with the Earthroamer's Ford chassis. Dick Smith's
		experience with his 6.0 PSD Earthroamer in Mongolia            
		    <a href="http://www.smithsoverland.com/travel/publish/article_90.php">see this link</a>
		 may be an extreme; but unlike Dick
		we don't have the money to fly Ford mechanics from the US to fix mechanical problems.</p>
		<p>It is worth noting that the Smiths and their Earthroamer did complete their trip 
		around-the-world and at least one other Earthroamer 
		<a href="http://globalroadtrekker.com/"> see globalroadtrekker.com</a>
		is currently undertaking such an adventure.
		So my concerns may well be "obsession" rather than realistic.</p>

        <h3>GVW, Tires and suspension</h3>
        <p>Vehicle GVW, tire load rating and suspension modification was the second topic that
        got me thinking of "another vehicle".</p>
        <p>Our Tiger was pretty much at maximum GVW all the time. That was not a result
        of us carrying a lot of stuff simply the result of the camper body
        on a 1-ton pickup. The consequence of that loading was that at the rear the tires were running
        at their load limit and wore relatively quickly. Had I stayed with the Tiger I would
        have considered an upgrade to 19.5" wheels like the 
        <a href="http://www.travelin-tortuga.com/Travelin-Tortuga/So,_whats_gone_wrong.html">
            travelin-tortuga.com
        </a>, 
        but that may have involved some suspension mods and maybe re-gearing.</p>
        <p>Our Earthroamer was also running close to GVW but at least with those 22.5
        wheels and Michelin tires there was plenty of excess tire load rating.</p>

        <h3>The initial wish list</h3>
        <p>I concluded that I should look for a camper built on a Mercedes truck
        with a GVW of over 20,000 lbs, tires that could handle our traveling weight
        and the vehicle GVW and no suspensions modifications.</p>
        <h3>Why a Mercedes?</h3>
        <p>The most colorful explanation of this I have ever read is from 
        <a href="http://www.jimrogers.com/">Jim Rogers</a>. He and his wife drove a yellow
        Mercedes sports car around the world in 1999, 2000, 2001. He argued (something like)
        "when dictators take over a country the first thing they do is buy (stolen)
        Mercedes cars". Hence even poor countries have Mercedes dealers.</p>
        <p>It is (probably?) not true that there are Mercedes truck repair facilities everywhere,
        and a Unimog is only a <em>"sort-of"</em> Mercedes. But I still bought the argument, that a Mercedes
        truck will be easier to get parts for than a US Ford or Chevy.</p> 

        <h3>GXV's Global Traveler</h3>

        <?php PostImage($trip, $slug,"global_traveler.png", "float:left; padding: 0px 15px 0px 0px;" );
        ?><p>In August of 2011 we visited the GXV factory in Missouri primarily to look at their
        <em>Global Traveler</em> vehicle. </p>
        <p>This is a "typical" European style expedition camper; a forward
        control truck with a camper box on the back. 
        The truck itself was a Mercedes 1017 with factory 4x4 and factory rear locker.
        The only modification to the truck was a conversion to 20" wheels all round and 
        singles (rather than duallies) at the rear. 
        This truck is commonly used by Europeans for expedition campers
        so I felt confident that it was a good candidate.</p>
        <p>In the flesh the truck was less than I had hoped for. No creature comforts 
        (the F550 Lariat package on our ER had somewhat spoiled us) including no A/C and no cruise control.
        A bit low on power and gearing for extended highway travel.</p>
        <p>If we chose this option GXV would have to source a truck for us in Europe and
        import it under the 25 year rule. The all up cost to get a 25 year old truck was 
        approaching that of a new F550.</p>
        <p>All in all the truck was not as strong a candidate as I had expected. But the camper
        box itself was great, well made with plenty of storage and top line equipment.</p>

        <h3>Enter the U500 vario-pilot</h3>

        <?php PostImage($trip, $slug,"u500-safari-1.jpg", "float:left; padding: 10px 15px 10px 10px;" );
        ?><p>The only other realistic option for a "Mercedes" here in the US was a 
        Unimog U500. We knew from previous conversations that Mike was very familiar with the
        U500 and thought very highly of them. Nina and I on the other hand had viewed them as a bit 
        O-T-T (over-the-top).</p>
        <p>While discussing all of this with Mike and Rene Van Pelt 
        they mentioned the <em>vario-pilot</em> feature available on some Unimog U500 trucks.</p>
        <p>For those that don't know about this (as we did not at the time) it is a little
        un-believable. It is a feature that allows the driving controls (steering wheel and pedals)
        to be moved from one side of the vehicle to the other. Moreover this transformation takes only
        a few minutes.</p>
        <p>This gave us the option to take the truck and camper back to Australia with us
        as the vario-pilot qualifies as right-hand drive. Other vehicles like the
        Chevy or Ford would probably require a steering conversion to be eligible
        for Australian registration.</p>
        <p>The vario-pilot was a significant factor in our final decision. Whether we
        will eventually take our U500-GXV back to Australia or not only time
        will tell. But the option to do so seemed valuable.</p>
        <p>Just to complete the picture. The U500 has a 33,000lb GVW, is factory 4x4,
        and comes with 395/85R20 tires which will carry the 33,000 GVW easily.</p>
        
        <h3>Lets go</h3>
        <p>So in late August 2011 Mike with the help of Rob Pickering found us a U500
        with the coveted vario-pilot and we were on our way to yet-another-expedition-vehicle
        (YAEV in the language of linux nerds).</p>
