<h2>Assessing the system</h2>
<p>After almost a years experience with the camper electrical systems I would have to say I am not very
impressed with the <strong>system design</strong>.</p> 
<p>The individual components are all "high quality" and largely "work as advertised"
but the total system does not seem well aligned with its original mission.</p>
<h3>The choice of an Outback Inverter/charger</h3>
<p>The Outback inverter charger is the core, or center piece, of the electrical system,
yet it seems to be a poor selection given the vehicles mission.</p>
<ul>
<li>
<p>The power output of 2800 watts from the inverter is far greater than required by any of the
appliances installed in the camper.</p>
<p> On reflection it seems clear that the inverter was initially sized
(that is AC power output was chosen) to power the camper air conditioning unit as a complement to a Fischer-Panda
DC generator that was initially specified for the camper.</p>
<p> In this initial scenario
the generator would only charge the batteries (or feed the inverter) and all AC loads including the
air conditioner would be powered by the inverter. The decision to replace a Fischer-Pander
with an Onan AC generator, and the decision to not power the camper A/C unit from the inverter would have allowed
the choice of a lower powered inverter, say 2000 watts. </p>
</li>
<li><p>A lower powered inverter would have allowed the choice of a standalone inverter rather
than an inverter/charger. It seems that high powered inverters are really only available
in an inverter/charger combined unit.</p></li>
<li>
<p>The Outback inverter/charger (like all inverter/chargers on the market) is not a 
multi-voltage device. US models can only make use of shore power of 110V while European models
require 230V.</p>
<p>Moreover the Outback is sensitive to the frequency of the shore power supply so
that it is not possible to run a US Outback inverter/charger on European power via a step
down transformer as even though this will correct the voltage transforming 230V to 110V
it will not correct the frequency. 
</p>
<p> Hence this inverter/charger seems a poor choice for a camper that has an international
mission as it cannot be connected to shore power in much of the world.
</p>
</li>
<li><p>In addition the single voltage nature of the inverter/charger forced the inclusion of a second
battery charger to allow charging of the house batteries from "foreign" shore power.</p></li>
<li><p>It is worth noting that the 
<a href="http://www.mastervolt.us/marine/products/mass-combi-12v/mass-combi-12-2500-100-120-v/">
Mastervolt Mass Combi 12/2500(120)
</a>, the
<a href="http://www.xantrex.com/documents/Inverter-Chargers/Freedom-SW-NewGen/FreedomSWInstallationGuide(97-0020-01-01_Rev-B).pdf">Xantrex Freedom SW New Generation</a> inverter/chargers, 
 and possibly the <a href="http://www.magnumenergy.com/Products/MSseries.htm">Magnum MS Series 2000</a>
while requiring 120V AC will tolerate input frequencies from 45-70/75Hz and thus <em>could</em> be fed 
from a 230V/50Hz supply 
via a step down transformer. </p></li>

</ul>
<p>My conclusion is that the choice of an Outback inverter/charger 
as the core of the system was a mistake,
and that the choice of a 2800 watt inverter was excessive.</p>
<h3>The "foreign" shore power charger is inadequate</h3>
<p>As discussed in a previous post on the electrical system the Mastervolt 12/35 charger
is totally inadequate as a charger for a 765 AH battery pack. Some of the money invested in the
Outback inverter/charger would have been better spent on a high powered multi-voltage
charger.</p>
<h3>A "better" system design - Version 1 </h3>
<p>So what might a "better" design look like?</p>
<ul>
<li>
<p>Make the battery charger(s) the core decision. These/this should be able to operate
    on any shore power supply, have at least the charging capacity of the Outback inverter/charger,
    and have selectable power demand for situations where
    shore power supplies have limited capacity.</p>
<p>There are a number of possible solutions
    available such as a Mastervolt 12/100 or a pair of paralleled Xantrex TrueCharger2 60amp.
 
    </p>
</li>
    <li><p>A 2000 watt standalone pure sine wave inverter (even a pair of them for backup).</p></li>
    <li><p>A separate AC transfer switch to replace the similar function provided by the
    Outback inverter/charger. Though to be honest I think this is an unnecessary function.
    The camper already requires us to use a manual selection switch to choose between 
    shore power and generator. It would not be a hardship to have a third position on 
    that selection switch for "inverter" power.</p></li>
    <li><p>Match the charger with a solar panel controller, systems controller, display panel
    and battery monitor from the supplier of the charger (Mastervolt or Xantrex).
    They both have entirely adequate products.</p></li>
</ul>
<p>A system like the one described above might be less expensive, certainly would be better value for money,
and certainly would be more functional.</p>
<h3>A "better" system design - Version 2</h3>
<p>Install a MasterVolt Mass Combi 12/2500(120) (or Xantrex Freedom SW or Magnum MS)  inverter charger 
and a 2000 watt 230V to 110V step down transformer.</p>
<p>Provide 2 shore power plugs on the vehicle.</p>
<ul>
<li><p> one of  which feed the Mass Combi with 110V/60Hz power directly, and </p></li>
<li><p>the other which feed 230V/50Hz power through the step down transformer to the Mass Combi.</p></li>
</ul>
<p> The only additional issue is how to protect appliances that cannot tolerate the incorrect frequency.
This would require some way of preventing the transfer switch (built into the Mass Combi) from operating
when foreign shore power was being used. </p>
<p>Personally I would not worry about this, most appliances are tolerant of frequency variations.
We used such an arrangement in South America and I know of travelers that have done the same in Europe with
a US made motorhome.</p>
