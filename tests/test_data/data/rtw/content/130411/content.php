		<p>This morning Sergei took us on a tour of some of the local sights in his off-road equipped Nissan Patrol.
		We were accompanied on this exercise by the lady teacher from last night (Ludmilla) and a man named Shasha
		First stop on this tour somewhat surprisingly was a large statue of a Buddha in a small village near
		Ussurisk. It seems that the area around the Buddha was inhabited 10,000 years ago by an ancient
		race and also occupied by the army of Genghis Khan. Equally interesting to us was the forest, 
		all the trees were coated in
		ice condensed out of the moist air by the overnight chill (haw frost).</p>
		<p>We did not anticipate that the next event on the days schedule was an interview with a couple of local reporters
		basically wanting to know why we were here and what we were doing. Good questions! This interview
		took place at the gates of an establishment called the <em>Emerald Valley</em> it is
		a Russian version of one of those US/Canadian Heritage Towns a bit amusement park and a bit historical.
		This one has been under construction for a number of years and is the brain child of our host Sergei.
		The scale of the undertaking, the imagination, and the construction was impressive.
		The park consists of amusements for children, places to picnic as well a reconstructed
		housing and small villages from various historical periods.</p>
		<p>A brief stop in the town center for the war memorial and one of the lovely local churches was followed
		by lunch as Sergei's house, a short escorted drive back the highway and then final goodbyes
		before we were on our way north. Sergei and his friends and family were altogether very hospitable 
		and went out of their way to be helpful to us crazy Australians - thank you.</p>
		<?php Skin::JournalGalleryThumbnails($trip, $entry); ?><p>Our next stop for the day was to be the town of Spassk (various spellings available) where we were
		to meet another of Vladimir's friends Andrey (and his daughter Lena). That connection proved
		remarkably easy, no sooner had we pulled off the road at the approach to the town than a car stopped in
		front of us and Andrey and Lena got out to greet us. They were to take us to a nearby branch
		of the Far East Academy of Science to see (possibly) one of the now rare Siberian Tigers. Andrey's
		first attempt to guide us to the town of Gayveron (where the institute is) was thwarted by 
		a 3.5m underpass and our 4.0m high vehicle. A detour through the center of town overcame that difficulty
		and soon we were on our way through open farm land towards Gayveron.</p>
		<p>Nina had read that it was difficult to visit the institute and that tourists can only get into this place
		as part of a tour. But is seemed Andrey had arrange a private visit for us and we were met at the gate 
		by the Institute's Director. We spent an interesting 30-40 minutes viewing the (now one and only) Tiger
		as well as Russian bears, foxes, lynxes (even one expecting a cub). One of the more intriguing demonstrations
		we witnessed was a male bear consuming a can of sweet condensed milk - the paper wrapper was removed from 
		around the can and then the bear simple punctured the can with his teeth and thereafter squeezed all
		the sweet gooey contents out, stopping every so often to enthusiastically lick up the expressed contents. </p>
		<p>After our return to the highway from Gayveron, Andrey and his family led us to a cafe and truck stop
		just north of town where we shared a meal and where after many goodbyes they departed and left us to spend the 
		night. We expect to spend many nights in Russia parked at a cafe amid cargo trucks. </p>
		<p>As a bit of a PS I should note that the drive from Ussurisk to Spassk was in itself very interesting
		due to the mix of conditions we encountered. While the road was generally sealed and in fair condition's we did 
		encounter a number of section under construction. These very wide sections of gravel became mini Baja 1000 races
		with trucks and cars 4 or 5 abreast and at times completely obscured by dust. Also interesting in the midst of
		what was a nice sunny spring day we ran into a fierce snow storm that within minutes caked our truck in
		white. 
		</p>
