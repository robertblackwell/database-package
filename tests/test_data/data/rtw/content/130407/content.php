	<h3>Friday</h3>
	<p>Friday morning we phoned a few people and made arrangements for the afternoon, explored the area around
	our hotel, ventured into a super market and picked up a supply of groceries.</p>
	<p>Our first contact of the day was Svetlana from the <a href="">Links Ltd</a> who are going to help us with 
	the port formailities. We gave her some copies of relevant documents and were told that we could expect 
	(if things go well) to get the truck Tuesday or Wednesday. Communication was no problem, she speaks almost perfect 
	English (it makes us feel so inadequate - only one language).</p>
	<p>A little later in the afternoon we met up with Yuriy and Elena, father and friend of Tatiana, the lady
	we dealt with at the Everett end of the shipping process. Yuriy spoke only a little English but Elena's
	English was good and she got the job for the next 4-5 hours of translating between Yuriy and Nina and I.
	They had come prepared to give us a tour of Vladivostok.</p>
	<p>Our first stop was <em>Tokarevskii Mayak(Lighthouse)</em>,
	which is at the end of a very narrow man made spit of land that protrudes off the southern end of 
	the peninsula of land that extends south from central Vladivostok (the peninsula I think is called the
	Egersheld). Our hosts pointed out that this was a popular summer spot for picnicking and swimming.
	But today with small remnants of winter ice decorating the shore we were all very well rugged up;
	no swimming.</p>
	<?php RenderNamedEntryGallery($trip, $slug, "friday"); ?><p>Back into the center of Vladivostok and an introduction to rush hour traffic Russian style
	(only too familiar to residents of Sydney or Seattle) and then south to Russkii (various spellings available) Island. The
	road to Russkii Island has only recently been completed (in time for the 2012 APEC Summit) and incorporates two very spectacular
	cable-stay bridges. The larger of the bridges which connects the mainland with the Island is the largest cable-stay
	bridge in the world (For our Australian readers it is like a very large version of the newish ANZAC Bridge
	over Glebe Island and Pyrmont). Russkii Island was once entirely a military facility but during our visit we
	only saw a few relics of fortifications from Czarist times and none from Soviet times. Now the island seems destined 
	to be opened for upmarket housing development and to be the new home for the Federal Far Eastern University.
	There is already a large campus complex in evidence even though not all of the university 
	facilities/functions have moved as yet. The 2012 APEC summit was held in the campuses large and impressive main hall. </p>
	<p>From Russkii Island we traveled (in a roughly northerly direction) along a new road east of Vladivostok
	to Yuriy's house where we changed vehicles (I will explain below). The house sits on about 1/3 acre of well treed
	land. The house is spacious with some really fine timber workmanship. But the more noticeable features of the
	house are the sauna, the fully functional Japanese wooden bathtub and the heated floors.</p>
	<p>Now geing chauffeured in Elena's right hand drive Toyota 4Runner rather than Yuriy's left hand drive SsangYong
	we headed for a local seaside restaurant. At dinner Nina got introduced to Russian tea (spiced with a selection of fruits),
	we both embarrased ourselves by not being able to finish our portions of the very ample meal, and Elena
	continued her fine performance as a translator.</p>
	<p>It was a great afternoon/evening, the visit to Russkii a treat and the warmth and hospitaility shown by
	Yuriy and Elena beyond all expectations. Thank you both.</p>
	<p>Early in our afternoon on our way to the Tokarevskii Lighthouse I spotted the ship <em>Grand Pace</em> moored just south
	along the dock facility from 
	Vladivostok's main railway station and parked on the dock beside it <em>our yellow truck!!</em>, so step
	one of the retreival process is complete, the vehicle has been unloaded.</p>
	<h3>Saturday</h3>
	<p>The weather for Saturday was forecast to be cool with snow and so it was, by 8:00 am the snow was falling lightly
	and as the day progressed the wind picked up, white caps appeared on the harbor, and the chill from
	the wind became cold rather than cool. Our room started to feel like a little warm haven in a windy cold world.</p>
	<p>We ventured out a few times during the day despite the weather. Mid morning I walked down to the dock 
	in the hope of getting a photo of the truck parked but could only get a couple of very poor pictures
	of parts of the truck. What I learned most on this walk was that wet snow, wind and glasses don't mix well. 
	Mid afternoon we walked down to the <em>Gum</em> (state owned department store) to buy Nina some winter boots
	as she had packed only joggers - and they certainly did not seem suitable for todays weather. We had expected
	the Gum to be an upmarket store selling a lot of international brands (that is what we had seen in Moscow
	6 years back) but the reality was a bit more like a market or outlet store. Regardless Nina found
	some fur lined boots that seemed to satisfy her.	</p>
	<p>At 6:00pm we walked down the hill from the hotel to meet Vladimir (the gentleman that met us at the airport)
	and his wife Nina (not a typo) for dinner. Dinner was both interesting and enjoyable. </p>
	<p>Vladimir it turns out is a retired military criminal investgator who now works with a private
	law firm handling criminal cases and Nina is a retired Emergency Medicine doctor. When asked why Nina
	retired from medicine Vladimir pointed out that his wife (about 5'1" and 100-110 lbs) found it difficult
	managing sick and injured people through 4 ft of snow (they had lived most of their working life
	on the Kamchatca Peninsula which is renowned for heavy snow falls.) </p>
	<p>Nina and Vladimir are avid 4x4ers and have traveled widely in Russia, Central Asia and Northern Europe
	in their Nissan Patrol and are planning an around the world adventure starting this summer (depending
	on Vladimirs current cases). They had a number of suggestions for us about places to see in Russia, Kazakhstan,
	and Armenia.</p>
	<p>The dinner was tough work for Vladimir as Nina B and I only speak English and Vladimir's Nina only Russian
	hence as the only bilingual at the table the job of translation fell to Vladimir. </p>
	<?php RenderNamedEntryGallery($trip, $slug, "saturday"); ?><p>An intersting culinary feature of the evening was a traditional combination of vodka, pickled herring (mixed with raw onion), 
	black bread and potato (chips or french fries). Vladimir gave me a Russian phrase for this combination
	but I have absolutely no chance of remembering it. Believe it or not I enjoyed it, the vodka (good quality) was
	reasonably smooth and the herring delicious.</p>
	<h3>Sunday</h3>
	<p>The weather today is even less pleasant. The snow has stopped, temperatures seem higher
	as the snow seems to be melting but the wind is blowing a gale.</p>
