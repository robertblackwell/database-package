	<p>As we now know riding condition on Canada's Dempster Highway were beyond my skill as a rider. Maybe if the weather
	had been kinder the conditions would have been within my means. But I am content enough to have tried and not been injured.
	From what I heard from other riders and locals not all that attempted the Dempster on motorcycles this year were so fortunate.
	The Dalton and Prudoe Bay served as a great consolation prize and my disappointment would have been much greater
	had I not made it to <em>Deadhorse</em>.</p>
	<p>The big positive outcome from this expedition was that my strategies for managing my body, and my previously mentioned shoulder pain.
	Modest daily mileage goals (200 miles per day), correct riding posture, and regular rest breaks with stretching exercises succeeded
	beyond my wildest dreams.</p>
	<p>Of course, the obvious question is; where next ?</p>
