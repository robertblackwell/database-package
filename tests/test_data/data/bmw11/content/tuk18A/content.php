    <p>Regular readers of this blog may recall that back in 2016 I acquired a BMW1200 GS motorcycle and not long after that
    acquisition I suffered a broken leg courtesy of that same motorcycle and my inexperience ( <a href="/posts/rtw/where_are_we">this link</a>).</p>
    
    <p>Not withstanding the questionable wisdom of motorcycles at my age, my interest in 2 wheels has continued. Earlier this year I completed a 10 day ride from
	Gig Harbor Washington to San Francisco, an enjoyable undertaking that has succeeded in further wetting my appetite for 2 wheels.</p>
	
	<h2>Adventure on 2 wheels - the road to Tuktoyaktuk</h2>
	
	<p>In a few days I will set forth on an attempt to ride (mostly solo) to the town of Tuktoyaktuk in Canada's Northwest Territory. 
	By way of interest this is the first summer that such a
	ride has been possible as only in the latter part of 2017 was an all weather road to this town opened. 
	In previous years the only road to Tuk (as the town is known)  
	was an <em>Ice Road</em> across the Mackenzie River and the Arctic Ocean. 
	A road which was obviously not open in the summer and which Nina and I drove April 17th 2009 in our Tiger (<a href="/entries/theamericas/090417">see here</a>).</p>
	
	<p>If my body survives the rigors of the ride to Tuk I will attempt to include the the Dalton Highway and a visit to Dead Horse Alaska in my itinerary.</p>
	   
	<h2>What about Nina and 4 wheeled adventures ?</h2>
	<p>I should note that this undertaking does not in any way signal the end of adventures by <em>Nina and Robert</em> on 4 wheels.</p>
	<p>If all goes according to plan, and our health holds out, we will be reporting on 3 new adventures on 4 wheels in the next 18 months.
	
	<h2>Steep learning curve</h2>
	<p>I must say that getting ready for this undertaking has been a bit of a learning curve.</p>
	<ul>
		<li><p>I have not camped in a tent or carried my gear in a backpack for 25 years. So coming to grips with 4-6 weeks living out of
			motorcycle panniers has been a bit of a struggle. The carry capacity of our Mog (effectively unlimited) has spoilt me.
		</p></li>
		<li><p>Oh, thats right I better buy and fit some panniers. I bought some from Touratech. They were a bit of a bear to install
			and I know I am going to hate the latches by the end of the trip. But I did not know what else to choose and did not want to
			spend any more money.
		</p></li>
		<li><p>Fuel range, Yikes. You mean I need a gas station every 200 miles? But the Mog can travel in excess of 1200 miles between pumps.
			You do know that from Dawson City to Eagle Plain it is 240 miles and there are NO gas stations in between.
		</p></li>
		<li><p>Use only premium gas ! At least I am not going to Uzbekistan or some other place where one buys fuel from road side vendors in coke bottles and the
			idea of asking for <em>premium</em> is not even a joke.</p></li>
		
		<li><p>All the reports I have read emphasis that the Dempster Highway is hard on tires, that punctures are to be expected and that <em>off road tires</em>
			are required to handle the mud and gravel. 
			<ul>
				<li><p>But, my current tires are for the street, so I should change them. And I should change them myself so I learn how to do it.
					The thing I learned is that removing and refitting a tire to a bike wheel is much easier with a lot of WD40 to lubricate the bead.
				</p></li>
				<li><p>But I don't have a spare, so I have to learn how to fix a puncture on the road side  with those tire plugs. </p></li>
				<li><p>Seems like carrying an inner tube for major tire problems will not work as the BMW has the valve offset from the center line at the
					rear and that this offset will destroy the inner tube once inflated. At least that's what I have read.  
				</p></li>
				<li><p>I also keep reading that off-road motorcycle tires (at least the rear tire) may be done in 2500-3000 miles. 
					That means I will need to buy tires for a BMW 1200 somewhere  in the <em>Yukon Territory (YT)</em> </p></li>
			</ul>
		<li><p>What do you mean that only a BMW Motorrad dealer can clear the "service required" warning on the bikes instrument panel, and that
			I need to do this every 6000 miles. You do know that there are NO Motorrad dealers in either the Yukon Territory or North West Territory. 
			Maybe I should have bought a Honda or Yamaha as the town of Whitehorse, YT has dealers for both those brands.</p></li>
		<li><p>Nina and I have never before carried a personal locator device like a <em>SPOT</em> or <em>Garmin inReach</em>, but this time I am on my own,
			I have a history of motorcycle mishaps, and Nina is nervous. So I have to shell out for an inReach and the required satellite subscription, plus learn how to use it,
			and teach Nina how to send messages to the device.
		</p></li>
		<li><p>There has also been a learning curve related to me. For some years my left shoulder blade area has developed a sharp pain after a long day of
			driving or riding and recently it has been bad enough to abort a ride like the one I am planning. So over recent weeks I have had visits with chiropractors,
			physical therapists, doctors, MRI machines and spent hours on youtube researching my "condition". Seems I have a neck problem that pinches a nerve and that
			the application of a regime of riding for one hour, then resting and doing neck and back exercises for 10 minutes, and being conscious of correct posture 
			may stave off the onset of the shoulder pain. I will soon know.
			 </p></li>
	</ul>
