			<p>When we eventually joined up with SR141 we found a tourist plaque that explained the contruction we had seem.
			It was a <em>Flume way</em> that in all extended 13 miles in length (5 miles along the canyon wall),
			was 4' deep and 5'4" wide and carried over 23,640,000 gallons of water per day. </p>
			<p>SR141 proved also to be a pleasant surprise as it continued our descent of the Dolores River 
			traveling amongst wonderfull red rock cliffs all the way to Gateway Canyon.</p>
			<p>After some chores in Fruita and Grand Junction on I-70 we turned off towards Aspen along SR82.
			At the small town of Basalt we turn slightly north onto FryingPan Road into the White River National Forest.
			We traveled beside a very fast flowing river, in a narrow valley, with more amazing red cliffs,
			in the midst of heavy rain fall before arriving at our camp for the night at Ruedi Resevoir Dam. </p>
