		<p>What finds and unexpected beauty and granduer we had today.</p>
			<p>This morning we continued on CO SR90 a few miles into Paradox Valley and
			past the small (very small) town of Bedrock. There we turned left (north) onto
			River Road and followed it alongside the Dolores River. Initially nothing more than a good gravel road this
			way quicky turned into a great and pleasant surprise. The Dolores River entered a narrow canyon and the 
			road simply followed beside the river at the bottom of the canyon for some miles. After a while
			we started seeing the remains of some kind of engineering structures protruding from the walls of the canyon.
			Wooden pieces that looked like the remains of some form of railway or the like were part way up the side of the sheer
			sandstone walls. Simply amazing! </p>
